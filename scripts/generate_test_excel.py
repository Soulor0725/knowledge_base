def build_excel():
    wb = Workbook()
    ws = wb.active
    ws.title = '功能测试用例'

    # 标题行
    ws.append(COLUMNS)
    for col in range(1, len(COLUMNS) + 1):
        cell = ws.cell(row=1, column=col)
        cell.fill = HEADER_FILL
        cell.font = HEADER_FONT
        cell.alignment = CENTER
        cell.border = BORDER

    # 数据行
    for case in CASES:
        main_module, sub_module, case_id, title, priority, case_type, pre, steps, expected = case
        row = [case_id, main_module, sub_module, title, priority, case_type, pre, steps, expected]
        ws.append(row)
        r = ws.max_row
        for col in range(1, len(COLUMNS) + 1):
            cell = ws.cell(row=r, column=col)
            cell.font = CELL_FONT
            cell.border = BORDER
            cell.alignment = LEFT if col >= 7 else CENTER
        # 优先级着色
        pcell = ws.cell(row=r, column=5)
        if priority == 'P0':
            pcell.fill = P0_FILL
        elif priority == 'P1':
            pcell.fill = P1_FILL
        else:
            pcell.fill = P2_FILL

    # 合并相同主模块、子模块单元格
    merge_ranges_main = []
    merge_ranges_sub = []
    start = 2
    for i in range(2, ws.max_row + 2):
        cur_main = ws.cell(row=i, column=2).value if i <= ws.max_row else None
        cur_sub = ws.cell(row=i, column=3).value if i <= ws.max_row else None
        prev_main = ws.cell(row=i - 1, column=2).value if i > 2 else None
        prev_sub = ws.cell(row=i - 1, column=3).value if i > 2 else None
        # 主模块合并
        if i > 2 and cur_main != prev_main:
            if i - 1 > start:
                merge_ranges_main.append((start, i - 1))
            start = i
    # 处理最后一段
    if ws.max_row >= start:
        merge_ranges_main.append((start, ws.max_row))

    # 子模块合并（在主模块内合并）
    sub_start = 2
    for i in range(3, ws.max_row + 2):
        cur_main = ws.cell(row=i, column=2).value if i <= ws.max_row else None
        cur_sub = ws.cell(row=i, column=3).value if i <= ws.max_row else None
        prev_main = ws.cell(row=i - 1, column=2).value
        prev_sub = ws.cell(row=i - 1, column=3).value
        if i <= ws.max_row and cur_main == prev_main and cur_sub == prev_sub:
            continue
        else:
            if i - 1 > sub_start:
                merge_ranges_sub.append((sub_start, i - 1))
            sub_start = i
    if ws.max_row >= sub_start:
        merge_ranges_sub.append((sub_start, ws.max_row))

    # 应用合并
    for start_r, end_r in merge_ranges_main:
        ws.merge_cells(start_row=start_r, start_column=2, end_row=end_r, end_column=2)
        cell = ws.cell(row=start_r, column=2)
        cell.fill = MODULE_FILL
        cell.font = MODULE_FONT
        cell.alignment = CENTER
    for start_r, end_r in merge_ranges_sub:
        ws.merge_cells(start_row=start_r, start_column=3, end_row=end_r, end_column=3)
        cell = ws.cell(row=start_r, column=3)
        cell.fill = SUB_FILL
        cell.font = SUB_FONT
        cell.alignment = CENTER

    # 列宽
    widths = [14, 14, 12, 28, 8, 8, 22, 38, 38]
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w

    # 行高
    ws.row_dimensions[1].height = 32
    for r in range(2, ws.max_row + 1):
        ws.row_dimensions[r].height = 60

    # 冻结首行
    ws.freeze_panes = 'A2'

    # 第二个 sheet：统计汇总
    ws2 = wb.create_sheet('用例统计')
    ws2.append(['主模块', '子模块数', '用例数', 'P0', 'P1', 'P2'])
    for col in range(1, 7):
        cell = ws2.cell(row=1, column=col)
        cell.fill = HEADER_FILL
        cell.font = HEADER_FONT
        cell.alignment = CENTER
        cell.border = BORDER

    # 按主模块统计
    from collections import defaultdict
    stats = defaultdict(lambda: {'subs': set(), 'total': 0, 'P0': 0, 'P1': 0, 'P2': 0})
    for case in CASES:
        m = case[0]
        stats[m]['subs'].add(case[1])
        stats[m]['total'] += 1
        stats[m][case[4]] += 1

    row_idx = 2
    for module, data in stats.items():
        ws2.append([module, len(data['subs']), data['total'], data['P0'], data['P1'], data['P2']])
        for col in range(1, 7):
            cell = ws2.cell(row=row_idx, column=col)
            cell.font = CELL_FONT
            cell.border = BORDER
            cell.alignment = CENTER
        row_idx += 1
    # 合计行
    total_subs = sum(len(d['subs']) for d in stats.values())
    total_all = sum(d['total'] for d in stats.values())
    total_p0 = sum(d['P0'] for d in stats.values())
    total_p1 = sum(d['P1'] for d in stats.values())
    total_p2 = sum(d['P2'] for d in stats.values())
    ws2.append(['合计', total_subs, total_all, total_p0, total_p1, total_p2])
    for col in range(1, 7):
        cell = ws2.cell(row=row_idx, column=col)
        cell.fill = MODULE_FILL
        cell.font = MODULE_FONT
        cell.alignment = CENTER
        cell.border = BORDER

    widths2 = [16, 12, 12, 8, 8, 8]
    for i, w in enumerate(widths2, 1):
        ws2.column_dimensions[get_column_letter(i)].width = w
    ws2.row_dimensions[1].height = 30

    wb.save(OUT_FILE)
    print(f'已生成: {OUT_FILE}')
    print(f'用例总数: {len(CASES)}')
    print(f'主模块数: {len(stats)}')
    print(f'子模块数: {total_subs}')


if __name__ == '__main__':
    build_excel()
